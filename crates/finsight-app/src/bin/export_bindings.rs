//! Exports TypeScript bindings for the frontend.
//! Invoked by `cargo run -p finsight-app --bin export_bindings` or `pnpm bindings`.
//!
//! Must be run from the repo root; the output path is relative to CWD.

fn main() -> anyhow::Result<()> {
    let builder = finsight_app::build_specta_builder();
    builder
        .export(
            specta_typescript::Typescript::default()
                .header("// AUTO-GENERATED by tauri-specta. Do not edit.\n"),
            "ui/src/api/bindings.ts",
        )
        .map_err(|e| anyhow::anyhow!("bindings export: {e}"))?;
    println!("bindings written to ui/src/api/bindings.ts");
    Ok(())
}
